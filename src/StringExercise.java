public class StringExercise {
    public static void main (String[] args) {

        // 1.a
        String education = "We don't need no education\nWe don't need no thought control\n";
        System.out.println(education);

        // 1.b
        String checkThis = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(checkThis);

        // 1.c
        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        //1.d
        String backslash = " I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!\n";
        System.out.println(backslash);
    }
}
